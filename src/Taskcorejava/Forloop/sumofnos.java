//4️⃣ Print the sum of first 10 natural numbers
package Taskcorejava.Forloop;

public class sumofnos {
    public static void main(String[] args) {
        int sum =0;
        for (int i = 1; i <= 10; i++) {
            sum +=i;

        }
        System.out.println("Sum of first 10 natural numbers are:"+sum);
    }
}
